    /**
     * Display a listing of the resource depending on selected categories.
     * 
     * @param  $categories  Array
     * @return \Illuminate\Http\Response
     */
    public function categoriesProducts($categories = false){

        $categories = $categories  ? explode(',', $categories) : [];
        
        if($categories)
           return Product::whereIn('category_id',$categories)->get();

        return  Product::all();

